
x <- c(10,1,3,5,15,2)
x

# sum using sum() function
sum(x)

# Product using prod function
prod(x)

# Cumulative sum using cumsum()
cumsum(x)

# Cumulative product
cumprod(x)

# Compute mean using mean() 
mean(x)

# Compute median using median()
median(x)

# Compute variance using var() 
var(x)

# Compute standard deviation using sd()
sd(x)

# To find the summary statistics
summary(x)
